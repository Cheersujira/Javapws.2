public class Triangle {
   public static void main(String[] args) {
    // ตั้งค่าตัวแปร
    int height = 9;
    int base = 15;
    double area;
    // สูตรคำนวน
    area = 0.5 * height * base;
    // แสดงพื้นที่ทั้งหมด
    System.out.println("ความสูงของสามเหลี่ยม : " + height);
    System.out.println("ความยาวของฐาน : " + base);
    System.out.println("พื้นที่รูปสามเหลี่ยม : " + area);
    }
}



